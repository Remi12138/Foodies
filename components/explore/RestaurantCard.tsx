import React from "react";
import { StyleSheet, View, Image, Text } from "react-native";
import { Restaurant } from "@/zustand/restaurant";
import { FontAwesome } from "@expo/vector-icons";
import { useThemeColor } from "@/hooks/useThemeColor";




function RestaurantCard({ item }: { item: Restaurant }) {
  const cardBackgroundColor = useThemeColor({ light: "#fff", dark: "#333" }, "background");
  const cardNameColor = useThemeColor({ light: "#333", dark: "#fff" }, "text");

  const imageUrl =
    item.imageUrl && item.imageUrl.trim() !== ""
      ? { uri: item.imageUrl }
      : require("@/assets/images/default_location.png");

  const renderStars = (rating: number) => {
    const stars = [];
    for (let i = 0; i < Math.floor(rating); i++) {
      stars.push(<FontAwesome key={i} name="star" size={14} color="#FFD700" />);
    }
    return stars;
  };

  return (

    <View  style={[
      styles.cardContainer,
      { backgroundColor: cardBackgroundColor },
    ]}>
      {/* Image Section */}
      <Image source={imageUrl} style={styles.image} />

      {/* Text Content */}
      <View style={styles.textContainer}>
        {/* Name */}
        <Text style={[
      styles.name,
      { color: cardNameColor },
    ]}>{item.name}</Text>

        {/* Ratings */}
        <View style={styles.ratingContainer}>{renderStars(item.rating)}</View>

        {/* Location */}
        <Text style={[
      styles.location,
      { color: cardNameColor },
    ]}>
          {item.location.city}, {item.location.state}
        </Text>
      </View>
    </View>
  );
}

const styles = StyleSheet.create({
  cardContainer: {
    borderRadius: 8,
    overflow: "hidden",
    marginVertical: 10,
    shadowColor: "#000",
    shadowOffset: { width: 0, height: 2 },
    shadowOpacity: 0.1,
    shadowRadius: 4,
    elevation: 2,
    alignItems: "center", // Center align text below the image
  },
  image: {
    width: "100%",
    height: 200, // Increased image size
    resizeMode: "cover",
  },
  textContainer: {
    padding: 10,
    alignItems: "center", // Center align text
  },
  name: {
    fontSize: 16,
    fontWeight: "bold",
    marginBottom: 4,
   // color: "#333",
  },
  ratingContainer: {
    flexDirection: "row",
    alignItems: "center",
    marginBottom: 4,
  },
  location: {
    fontSize: 14,
   // color: "#666",
  },
});

export default RestaurantCard;
